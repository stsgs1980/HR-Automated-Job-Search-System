/**
 * QUALITY ATS — анализ ATS-совместимости резюме.
 * Проверяет, пройдёт ли резюме через автоматические фильтры hh.ru.
 *
 * ATS-фильтры проверяют:
 *  - Ключевые слова из целевой позиции в навыках и описаниях
 *  - Достаточно ли навыков (ATS ищет по совпадению навыков)
 *  - Контактная информация (ATS должен уметь связаться)
 *  - Структурированность данных (разделы на месте)
 *  - Описания к опыту (ATS парсит текст описаний)
 */

/**
 * Анализ ATS-совместимости.
 * @returns {{ score: number, checks: Array, earned: number, total: number }}
 */
export function analyzeATS(r) {
  const checks = [];
  let earned = 0;
  let total = 0;

  const add = (label, weight, passed, tip) => {
    checks.push({ label, weight, passed, tip });
    total += weight;
    if (passed) earned += weight;
  };

  // ── Контактная информация ──
  add('Контакты (телефон или email)', 12,
    !!(r.phone || r.email),
    'Укажите хотя бы телефон или email — ATS не сможет связаться'
  );

  // ── Навыки — основной критерий поиска в ATS ──
  const skillCount = (r.skills || []).length;
  add('Навыки указаны', 15,
    skillCount > 0,
    'Без навыков ATS не найдёт ваше резюме по ключевым словам'
  );
  add('Навыки ≥ 5 (поисковый порог)', 8,
    skillCount >= 5,
    'Меньше 5 навыков — ATS-поиск не покажет вас по большинству запросов'
  );
  add('Навыки ≥ 10 (хорошее покрытие)', 5,
    skillCount >= 10,
    '10+ навыков значительно расширяют поисковую видимость'
  );

  // ── Заголовок позиции ──
  add('Заголовок позиции', 10,
    !!(r.title && r.title.length > 2),
    'Без должности ATS не сможет отнести вас к нужной категории'
  );

  // ── Зарплата ──
  add('Зарплатные ожидания', 5,
    !!r.salary,
    'Многие работодатели фильтруют по зарплате — без неё вы выпадаете из фильтра'
  );

  // ── Локация ──
  add('Локация/город', 5,
    !!(r.address && r.address.length > 2),
    'Укажите город — иначе фильтр по локации вас отсечёт'
  );

  // ── Опыт работы ──
  const expCount = (r.experience || []).length;
  add('Опыт работы указан', 12,
    expCount > 0,
    'Без опыта ATS не может отфильтровать по требуемому стажу'
  );

  // ── Описания к опыту ──
  const expWithDesc = (r.experience || []).filter(e => e.description && e.description.length > 30);
  add('Описания к опыту', 10,
    expCount > 0 && expWithDesc.length > 0,
    'Без описаний ATS не найдёт ключевые слова в вашем опыте'
  );
  add('Все позиции с описанием', 5,
    expCount > 0 && expWithDesc.length === expCount,
    'Каждая позиция должна иметь описание — иначе ATS пропускает ключевые слова'
  );

  // ── Образование ──
  add('Образование', 5,
    (r.education || []).length > 0,
    'Многие ATS-фильтры требуют указанного образования'
  );

  // ── Занятость и формат ──
  add('Тип занятости', 5,
    !!r.employmentType,
    'Укажите тип занятости — работодатель фильтрует по нему'
  );
  add('Формат работы', 3,
    !!r.workFormat,
    'Удалённая/офис/гибрид — популярный фильтр на hh.ru'
  );

  const score = total > 0 ? Math.round((earned / total) * 100) : 0;
  return { score, checks, earned, total };
}
